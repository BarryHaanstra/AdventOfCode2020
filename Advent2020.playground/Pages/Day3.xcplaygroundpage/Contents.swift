func countWith(stepsX stepX: Int, stepsY stepY: Int) -> Int {
    let startX = 0
    let startY = 0

    // Split string by newlines, get map definition in array
    let inputArray = input.split(whereSeparator: \.isNewline)
    
    // Calculate how many times the pattern needs to be repeated for a wider map that works
    let mapLength = inputArray.count
    let mapWidth = inputArray.first!.count
    let endPosX = 1 + (stepX * mapLength)
    let repeatTimes = Int(ceil(Double(endPosX) / Double(mapWidth)))
    
    // Construct wider map
    let mapArray = inputArray.map {
        return String(repeating: String($0), count: repeatTimes)
    }
    
    // Test how many trees (#) are found on the way down
    var treeCount = 0
    var posX = startX
    var posY = startY
    
    while posY < mapLength {
        let treeLine = mapArray[posY]
        let mapSquareIndex = treeLine.index(treeLine.startIndex, offsetBy: posX)
        let mapSquare = treeLine[mapSquareIndex]
        
        if (mapSquare == "#") {
            treeCount += 1
        }
        
        // Advance
        posX += stepX
        posY += stepY
    }
    
    return treeCount
}

